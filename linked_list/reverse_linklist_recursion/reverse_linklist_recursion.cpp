Node *reverseLinkedListRec(Node *head)
{
    //Write your code here
    if(head->next==NULL)
        return head;
    Node *temp=reverseLinkedListRec(head->next);
    Node *temp1=temp;
    while(temp1->next!=NULL)
    {
        temp1=temp1->next;
        cout<<temp1->data<<" ";
    }
    cout<<head->data<<"\n";
    temp1->next=head;
    head->next=NULL;
    return temp;
}

